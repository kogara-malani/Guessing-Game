public class Assignmentoperator {
    public static void main(String[] args){
        int m =  10 , n = 20 , p = 0;
        p = m + n;
        System.out.println("p = m + n : " + p);
        p += m;
        System.out.println("p += m : " + p);
        p -= m;
        System.out.println("p -= m : " + p);
        p *= m;
        System.out.println("p *= m " + p);
        p /= m;
        System.out.println("p /= m : " + p);
        p %= m;
        System.out.println("p %= m : " + p);

    }
    
}
