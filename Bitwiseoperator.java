public class Bitwiseoperator {
    public static void main(String[] args){
        int a = 25;
        int b = 15;
        int ans = a|b;
        System.out.println(ans);
        int ans1 = a&b;
        System.out.println(ans1);
        int ans2 = a^b;
        System.out.println(ans2);
        int ans3 = ~ b;
        System.out.println(ans3);
        int c = a & b;
        System.out.println(c);
        

    }
    
}
